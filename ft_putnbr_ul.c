#include "libft.h"

void	ft_putnbr_ul(unsigned long int n)
{
	if (n >= 10)
	{
		ft_putnbr_u(n / 10);
		ft_putnbr_u(n % 10);
	}
	else
	{
		n += 48;
		write(1, &n, 1);
	}
}
